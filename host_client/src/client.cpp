/**
 * @file client.cpp
 * @brief Host EDHOC Initiator + OSCORE client for the ZBook usb_ncm_net
 *        suite-0 (X25519) build -- the peer counterpart of the device firmware,
 *        built from the SAME libraries (libedhoc for EDHOC, uoscore-uedhoc for
 *        OSCORE) so interop is guaranteed at the encoder level.
 *
 * Adapted from uoscore-uedhoc/samples/linux_edhoc_oscore/initiator_client: the
 * UDP socket + cantcoap transport + OSCORE loop are reused; the (buggy) uEDHOC
 * engine is replaced by libedhoc's synchronous suite-0 initiator, and the RFC
 * 9668 transport framing (0xf5 on message_1, C_R on message_3) is added.
 *
 * Flow: message_1_compose -> POST /.well-known/edhoc -> message_2_process ->
 * message_3_compose -> POST -> message_4_process -> export_oscore_session ->
 * oscore_context_init -> OSCORE-protected GET/PUT hello|led.
 *
 * Creds come from edhoc_creds_client.h (generated by scripts/edhoc_keys.py;
 * must match the flashed firmware's edhoc_creds.h).
 *
 * @copyright Copyright (c) 2026 Centro de Inovacao EDGE
 * SPDX-License-Identifier: Apache-2.0
 */

